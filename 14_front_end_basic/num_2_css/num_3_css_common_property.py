"""
    1. Color颜色属性:
        赋值方式:
            1.1 color:颜色对应英文单词
            1.2 color:#000000~#FFFFFF 前两位: 红色位, 中间两位: 绿色位, 最后两位: 蓝色位
            1.3 color:rgb(0.0.0) ~ rgb(255.255.255) >> rgb(红色位, 绿色位, 蓝色位)
            1.4 color:rgba(0.0.0.0) ~ rgb(255.255.255.1) >> rgb(红色位, 绿色位, 蓝色位, 透明度(0: 全透明, 1: 不透明))
    2. Font字体属性:
        2.1 字体样式: font-style
        2.2 字体加粗: font-weight:bold
        2.3 设置字体大小: font-size:30px
        2.4 设置字体类型: font-family:字体类别
    3. text文本属性:
        3.1 宽度: width
        3.2 字间距: letter-spacing, 单位px
        3.3 首行缩进: text-indent, 单位em(字符)
        3.4 设置阴影: text-shadow
    4. background背景属性:
        4.1 背景颜色: background-color
        4.2 背景图片: background-image:url(图片路径)
        4.3 背景大小: background-size
        4.4 背景平铺: background-repeat
        4.5 背景定位: background-position
    5. Border边框属性:
        5.1 边框厚度: border-width
        5.2 边框类型: border-style
        5.3 边框颜色: border-color
        5.4 边框线位置: 上-border-top, 下-border-bottom, 左-border-left, 右-border-right
        5.5 边框圆角设置: border-radius
        5.6 边框阴影效果: box-shadow
    6. 内外补白(填充):
        6.1 padding: 内填充
        6.2 margin: 外填充
    7. position定位属性:
        7.1 绝对定位: position:absolute // 针对整个html页面而言
        7.2 固定定位: position:fixed // 随网页滑动位置不变
        7.3 相对定位: position:relative
    8. layout布局属性:
        8.1 display
        8.2 float
        8.3 clear
        8.4 overflow
"""