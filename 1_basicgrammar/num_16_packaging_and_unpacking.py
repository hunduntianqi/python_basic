"""
    组包:
        将多个值同时赋给一个变量时,解释器会进行自动组包操作, 使用一个元组来接收数据
    拆包:
        将一个容器变量里面的多个数据同时赋值给多个变量, 解释器会进行拆包操作
        注意:
            1. 拆包时被赋值变量个数要和容器中数据个数一样, 否则程序会报错
            2. 字典在拆包时赋值给变量的数据时字典的key_name, 获取字典的value需要使用 dict_name[key_name]
"""

# 组包
data_packaging = 1, 2, 3, 4, 5
print(data_packaging)  # (1, 2, 3, 4, 5)

# 拆包
a, b, c, d, e = data_packaging
print(a, b, c, d, e)  # 1 2 3 4 5
